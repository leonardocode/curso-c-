using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace VariablesyEnteros
{
    public class Program
    {
        static void Main(string[] args)
        {
            //short
            //desde - 32.768 a 32.767
            short a = 20;

            //int
            //desde -2.147.843.648 a 2.147.483.647
            int b = 40000;

            //long
            //De -9.223.372.036.854.775.808 a 9.223.372.036.854.775.807
            long c = 1000000000000;

            //double
            //Nos permite almacenar números y decimales (7 dígitos)
            double d = 1459.67585858;

            //float
            //Nos permite almacenar números y decimales (15-16 dígitos)
            float e = 30.6f;

            //decimal
            //Nos permite almacenar decimales , es mas preciso de todos(28-29 dígitos)
            decimal f = 3094567788888876549.6m;


            //char
            //representa un caracter
            char g = 'g';

            //string
            string nombre = "leonardo";


            //boolean
            bool exito = false;


            Console.ReadKey();






        }
    }
